public class Catego {
    int n;
    int number;

    public Catego(){}

    public Catego(int n, int number){
        this.n = n;
        this.number = number;
    }

    public int getN() {
        return n;
    }

    public void setN(int n) {
        this.n = n;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }
}


